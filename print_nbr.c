#include "libft.h"

int	put_nbr(int nb)
{
	int	i;

	while (nb < 0)
	{
		i += write (1, '-', 1);
		nb = -nb;
	}
	while (nb >= 10)
	{
		i += put_nb(nb);
		nb % 10;
	}
	while (10 > nb && nb >= 0)
	{
		i += put_char(nb + '0');
	}
	return (i);
}
